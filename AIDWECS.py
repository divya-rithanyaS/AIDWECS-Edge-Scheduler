import numpy as np
import random
import pandas as pd
import time
import matplotlib.pyplot as plt
import seaborn as sns
from typing import List, Dict, Tuple

# ----------------- 1. ENVIRONMENT & MODEL DEFINITIONS -----------------

# Heterogeneous Edge Servers (Realistic Setup)
SERVERS = [
    {"id": 1, "GHz": 2.0, "Power": 10}, # 2.0 GHz, 10W
    {"id": 2, "GHz": 3.0, "Power": 15}, # 3.0 GHz, 15W
    {"id": 3, "GHz": 1.5, "Power": 7.5} # 1.5 GHz, 7.5W
]

# Action space for the RL Agent (AIDWECS)
ACTIONS = [
    {"w_time": 0.8, "w_energy": 0.2}, # Strongly prioritize time/deadline
    {"w_time": 0.6, "w_energy": 0.4}, # Prioritize time
    {"w_time": 0.4, "w_energy": 0.6}, # Prioritize energy
    {"w_time": 0.2, "w_energy": 0.8}, # Strongly prioritize energy
]

# Global Q-table for the AIDWECS agent
Q_TABLE = {}

# ----------------- 2. TASK & STATE MANAGEMENT -----------------

def generate_random_tasks(num_tasks: int = 10) -> List[Dict]:
    """
    Generates a batch of random tasks.
    Includes 'arrival_time' for FCFS comparison.
    """
    tasks = []
    current_time = 0
    for i in range(num_tasks):
        load = random.choice([2e9, 4e9, 6e9, 8e9, 10e9])
        deadline_offset = random.randint(4000, 10000)
        arrival_time = current_time
        tasks.append({
            "id": i + 1,
            "load": load,
            "deadline": arrival_time + deadline_offset,
            "arrival_time": arrival_time
        })
        current_time += random.randint(100, 500)
    return tasks

def discretize_state(tasks: List[Dict]) -> Tuple[int, int]:
    """
    Discretizes the system state into (Urgency_Bin, Load_Bin).
    """
    if not tasks:
        return (0, 0)

    total_tasks = len(tasks)
    avg_deadline = sum(t["deadline"] - t["arrival_time"] for t in tasks) / total_tasks
    avg_load = sum(t["load"] for t in tasks) / total_tasks

    Urgency_Bin = 1 if avg_deadline < 6500 else 0
    Load_Bin = min(2, int(avg_load // 3.5e9))

    return (Urgency_Bin, Load_Bin)

# ----------------- 3. SCHEDULER IMPLEMENTATIONS -----------------

def base_scheduler(tasks: List[Dict], weights: Dict = None, log_output: bool = False) -> Tuple:
    """
    A generic scheduling engine. It assigns tasks to the best server based on a cost function.
    If no weights are provided, it defaults to picking the server that finishes the task earliest.
    """
    server_finish_times = {s["id"]: 0 for s in SERVERS}
    total_energy = 0
    completed = 0
    logs = []

    max_exec_time = 1
    max_energy = 1
    if weights and tasks:
        max_exec_time = max(t["load"] / (s["GHz"] * 1e9) * 1000 for t in tasks for s in SERVERS)
        max_energy = max((t["load"] / (s["GHz"] * 1e9)) * s["Power"] for t in tasks for s in SERVERS)
        if max_energy == 0: max_energy = 1

    for t in tasks:
        best_cost = float("inf")
        best_server = None
        best_details = {}

        for s in SERVERS:
            exec_time_ms = t["load"] / (s["GHz"] * 1e9) * 1000
            energy_J = (exec_time_ms / 1000) * s["Power"]

            available_at = max(server_finish_times[s["id"]], t['arrival_time'])
            finish_time = available_at + exec_time_ms

            cost = finish_time

            if weights:
                norm_time = finish_time / (available_at + max_exec_time) if (available_at + max_exec_time) > 0 else 0
                norm_energy = energy_J / max_energy
                cost = weights["w_time"] * norm_time + weights["w_energy"] * norm_energy

            if cost < best_cost:
                best_cost = cost
                best_server = s
                best_details = {"finish_time": finish_time, "exec_time": exec_time_ms, "energy": energy_J}

        if best_server:
            server_finish_times[best_server["id"]] = best_details["finish_time"]
            total_energy += best_details["energy"]

            status = "✔" if best_details["finish_time"] <= t["deadline"] else "✘"
            if status == "✔": completed += 1

            if log_output:
                logs.append((t["id"], best_server["id"], round(best_details["exec_time"], 2),
                             round(best_details["energy"], 4), status,
                             round(best_details["finish_time"], 2), t["deadline"]))

    completion_rate = completed / len(tasks) if tasks else 0
    return completion_rate, total_energy, completed, logs

# --- Specific Implementations for each Scheduling Algorithm ---

def schedule_fcfs(tasks, log_output=False):
    """First-Come, First-Served"""
    sorted_tasks = sorted(tasks, key=lambda x: x['arrival_time'])
    return base_scheduler(sorted_tasks, log_output=log_output)

def schedule_sjf(tasks, log_output=False):
    """Shortest Job First"""
    sorted_tasks = sorted(tasks, key=lambda x: x['load'])
    return base_scheduler(sorted_tasks, log_output=log_output)

def schedule_edf(tasks, log_output=False):
    """Earliest Deadline First"""
    sorted_tasks = sorted(tasks, key=lambda x: x['deadline'])
    return base_scheduler(sorted_tasks, log_output=log_output)

def schedule_dwecs(tasks, log_output=False):
    """DWECS with Static, Balanced Weights"""
    sorted_tasks = sorted(tasks, key=lambda x: x['deadline'])
    static_weights = {"w_time": 0.5, "w_energy": 0.5}
    return base_scheduler(sorted_tasks, weights=static_weights, log_output=log_output)

def schedule_aidwecs(tasks, weights, log_output=False):
    """AI-Driven DWECS with Learned Weights"""
    sorted_tasks = sorted(tasks, key=lambda x: x['deadline'])
    return base_scheduler(sorted_tasks, weights=weights, log_output=log_output)


# ----------------- 4. AIDWECS TRAINING -----------------

def train_aidwecs_agent(episodes: int = 20000):
    """Training loop for the AI-Driven Scheduler (AIDWECS)."""
    LR = 0.1
    GAMMA = 0.9

    print(f"Training AIDWECS agent for {episodes} episodes...")
    for episode in range(episodes):
        tasks = generate_random_tasks()
        state = discretize_state(tasks)

        if state not in Q_TABLE:
            Q_TABLE[state] = np.zeros(len(ACTIONS))

        epsilon = max(0.05, 1.0 - episode / (episodes * 0.8))
        if random.random() < epsilon:
            action_index = random.randint(0, len(ACTIONS) - 1)
        else:
            action_index = np.argmax(Q_TABLE[state])

        action_weights = ACTIONS[action_index]
        _, energy, completed_tasks, _ = schedule_aidwecs(tasks, action_weights)

        # --- **UPDATED REWARD FUNCTION** ---
        # Heavy penalty for each failed task to prioritize completion rate
        failed_tasks = len(tasks) - completed_tasks
        reward = (completed_tasks * 10) + (failed_tasks * -25) - (energy * 0.01)

        old_value = Q_TABLE[state][action_index]
        next_max = np.max(Q_TABLE[state])
        new_value = old_value + LR * (reward + GAMMA * next_max - old_value)
        Q_TABLE[state][action_index] = new_value

        if (episode + 1) % 5000 == 0:
            print(f"  Episode {episode+1}/{episodes} completed.")

    print("Training Complete.\n")

# ----------------- 5. EVALUATION AND VISUALIZATION -----------------

def evaluate_all_schedulers(test_tasks: List[Dict]):
    """
    Evaluates all schedulers on the same set of test tasks and visualizes results.
    """
    print("----- Evaluating Schedulers on a Fixed Set of Tasks -----")

    state = discretize_state(test_tasks)
    if state not in Q_TABLE:
        print("Test state not seen during training, defaulting to balanced action.")
        action_index = 1
    else:
        action_index = np.argmax(Q_TABLE[state])

    chosen_weights = ACTIONS[action_index]
    print(f"AIDWECS Optimal Action for this state: {chosen_weights}")

    schedulers = {
        "FCFS": schedule_fcfs, "SJF": schedule_sjf, "EDF": schedule_edf,
        "DWECS (Static)": schedule_dwecs,
        "AIDWECS (Ours)": lambda tasks, log_output: schedule_aidwecs(tasks, chosen_weights, log_output)
    }

    results = []
    for name, func in schedulers.items():
        start_time = time.perf_counter()
        rate, energy, _, logs = func(test_tasks, log_output=(name == "AIDWECS (Ours)"))
        cost_ms = (time.perf_counter() - start_time) * 1000

        results.append({
            "Scheduler": name, "Task Completion (%)": rate * 100,
            "Total Energy (J)": energy, "Computational Cost (ms)": cost_ms
        })

        if name == "AIDWECS (Ours)" and logs:
            print("\n--- Detailed Log for AIDWECS ---")
            print(pd.DataFrame(logs, columns=["Task", "Server", "Exec Time", "Energy", "Status", "Finish", "Deadline"]).to_markdown(index=False))


    results_df = pd.DataFrame(results).sort_values(by="Task Completion (%)", ascending=False)
    print("\n\n--- Comparison Summary ---")
    print(results_df.to_markdown(index=False))

    fig, axes = plt.subplots(1, 3, figsize=(20, 6))
    fig.suptitle('Performance Comparison of Scheduling Algorithms', fontsize=16, fontweight='bold')
    sns.set_style("whitegrid")

    sns.barplot(x="Scheduler", y="Task Completion (%)", data=results_df, ax=axes[0], palette="viridis").set_title('Task Completion Rate', fontsize=14)
    axes[0].tick_params(axis='x', rotation=45)

    sns.barplot(x="Scheduler", y="Total Energy (J)", data=results_df.sort_values("Total Energy (J)"), ax=axes[1], palette="plasma").set_title('Total Energy Consumption', fontsize=14)
    axes[1].tick_params(axis='x', rotation=45)

    sns.barplot(x="Scheduler", y="Computational Cost (ms)", data=results_df.sort_values("Computational Cost (ms)"), ax=axes[2], palette="magma").set_title('Scheduler Computational Cost', fontsize=14)
    axes[2].tick_params(axis='x', rotation=45)

    plt.tight_layout(rect=[0, 0.03, 1, 0.95])
    plt.show()

# --- EXECUTION ---
# 1. Train the improved AI agent
train_aidwecs_agent(episodes=20000)

# 2. Generate a final, fixed batch of tasks for a fair comparison
final_test_tasks = generate_random_tasks(num_tasks=10)
print("\n--- Generated Test Tasks ---")
print(pd.DataFrame(final_test_tasks)[['id', 'load', 'arrival_time', 'deadline']].to_markdown(index=False))

# 3. Evaluate and compare all models
evaluate_all_schedulers(final_test_tasks)

