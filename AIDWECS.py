# ----------------- 2. TASK & STATE MANAGEMENT -----------------

def generate_low_stress_tasks(num_tasks: int = 15) -> List[Dict]:
    """
    Generates a batch of tasks with relaxed deadlines to ensure all schedulers
    can achieve a 100% completion rate, isolating energy as the key variable.
    """
    tasks = []
    arrival_time = 0
    for i in range(num_tasks):
        load = random.choice([2e9, 4e9, 6e9]) # Lighter loads
        # Very relaxed deadlines (15-25 seconds)
        deadline_offset = random.randint(15000, 25000)
        tasks.append({
            "id": i + 1, "load": load,
            "deadline": arrival_time + deadline_offset,
            "arrival_time": arrival_time
        })
        arrival_time += random.randint(200, 500)
    return tasks

def discretize_state(tasks: List[Dict]) -> Tuple[int, int]:
    if not tasks: return (0, 0)
    avg_urgency = sum((t['deadline'] - t['arrival_time']) for t in tasks) / len(tasks)
    # For this experiment, urgency will always be low (0)
    urgency_bin = 1 if avg_urgency < 10000 else 0
    return (urgency_bin,) # State is now simpler, just based on urgency

# ----------------- 3. SCHEDULING ENGINE -----------------

def scheduling_engine(tasks: List[Dict], weights: Dict = None) -> Dict:
    server_finish_times = {s["id"]: 0.0 for s in SERVERS}
    server_tasks_count = {s["id"]: 0 for s in SERVERS}
    total_energy = 0.0
    completed_count = 0

    max_exec_time, max_energy = 1, 1
    if weights and tasks:
        max_exec_time = max(t["load"] / (s["GHz"] * 1e9) * 1000 for t in tasks for s in SERVERS)
        max_energy = max((t["load"] / (s["GHz"] * 1e9)) * s["Power"] for t in tasks for s in SERVERS)

    for t in tasks:
        best_cost, best_server, best_details = float("inf"), None, {}
        for s in SERVERS:
            exec_time_ms = t["load"] / (s["GHz"] * 1e9) * 1000
            energy_J = (exec_time_ms / 1000) * s["Power"]
            available_at = max(server_finish_times[s["id"]], t['arrival_time'])
            finish_time = available_at + exec_time_ms

            cost = finish_time # Default for EDF, FCFS, etc.
            if weights: # For DWECS and AIDWECS
                norm_time = finish_time / (available_at + max_exec_time) if (available_at + max_exec_time) > 0 else 0
                norm_energy = energy_J / max_energy if max_energy > 0 else 0
                cost = weights["w_time"] * norm_time + weights["w_energy"] * norm_energy

            if cost < best_cost:
                best_cost, best_server = cost, s
                best_details = {"finish": finish_time, "energy": energy_J}

        if best_server:
            server_finish_times[best_server["id"]] = best_details["finish"]
            server_tasks_count[best_server["id"]] += 1
            total_energy += best_details["energy"]
            if best_details["finish"] <= t["deadline"]:
                completed_count += 1

    return {
        "Completion Rate (%)": (completed_count / len(tasks)) * 100 if tasks else 0,
        "Total Energy (J)": total_energy,
        "Server Allocation": server_tasks_count
    }

